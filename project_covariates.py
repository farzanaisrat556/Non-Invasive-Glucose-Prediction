import pandas as pd

path = "/content/drive/MyDrive/Book1.csv"


demo_data = pd.read_csv(path, keep_default_na=False)




# Select the categorical columns to be one-hot encoded
cat_cols = ['Gender', 'CriticalDisease', 'Diabetes', 'Diabetic_status', 'Exercise', 'State']

# Perform one-hot encoding on the categorical columns
one_hot_encoded = pd.get_dummies(demo_data[cat_cols])



# Combine the one-hot encoded features with the numerical features
covariates = pd.concat([demo_data[['Age', 'Glucose Level on Glucose Meter']], one_hot_encoded], axis=1)

# Print the dimensions of the covariates dataframe
print(covariates.shape)

# Output file path
covariates.to_csv('covariates200.csv', index=False)

